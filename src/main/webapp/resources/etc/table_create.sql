DROP TABLE NOTICE;
DROP TABLE MEMBER;

CREATE TABLE MEMBER(
    MEMBER_IDX NUMBER(4) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MEMBER_LEVEL NUMBER(1) NOT NULL,
    LOGIN_ID VARCHAR2(255) NOT NULL,
    LOGIN_PW VARCHAR2(255) NOT NULL,
    NAME VARCHAR2 (255),
    REGIST_DATE DATE NOT NULL,
    POST_CODE VARCHAR2(255) NOT NULL,
    ADDR VARCHAR2(255) NOT NULL,
    ADDR_DETAIL VARCHAR2(255) NOT NULL,
    EMAIL VARCHAR2 (255),
    NOTE VARCHAR2 (255),
    PHONE VARCHAR2 (255)
);

CREATE TABLE NOTICE_BOARD(
    NOTICE_IDX NUMBER(5) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,      
    TITLE VARCHAR2(255),           
    CONTENT CLOB,    
    REGIST_DATE TIMESTAMP DEFAULT SYSTIMESTAMP,
    VIEWS NUMBER(5),
    MEBER_IDX NUMBER(4) REFERENCES MEMBER(IDX)    
);

CREATE TABLE FREEBOARD(
    FREEBOARD_IDX NUMBER(5) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITLE VARCHAR2 (255),
    CONTENT CLOB,
    REGIST_DATE TIMESTAMP DEFAULT SYSTIMESTAMP,
    VIEWS NUMBER(5),
    MEMBER_IDX NUMBER(4) REFERENCES MEMBER(IDX)
);

-- '이번달 일정표' 메뉴에 출력될 테이블 명은 attraction으로 지정.
CREATE TABLE ATTRACTION(
    ATTR_IDX NUMBER(5) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DETAIL VARCHAR2(255),
    PROG_TIME DATE,
    MEMBER_IDX NUMBER(5) REFERENCES MEMBER(MEMBER_IDX)
);
